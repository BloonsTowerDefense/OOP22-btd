package btd.view;

/**
 * An enumeration representing the different states of the game.
 */
public enum GameCondition {
    /**
     * The game is running.
     */
    PLAY,
    /**
     * The game is finished.
     */
    OVER,
    /**
     * Main menu.
     */
    MENU,
    /**
     * Exit the game.
     */
    EXIT;
}
