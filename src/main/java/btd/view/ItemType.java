package btd.view;

/**
 * Represents the different types of items that can have associated textures.
 */
public enum ItemType {

    /**
     * Represents a red bloon item type.
     */
    RED_BLOON,

    /**
     * Represents a blue bloon item type.
     */
    BLUE_BLOON,

    /**
     * Represents a black bloon item type.
     */
    BLACK_BLOON;
}

